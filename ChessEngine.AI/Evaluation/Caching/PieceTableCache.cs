        private const int BaseWhenReadingWhite = BoardConsts.BoardSize - 1;

        public static int GetClassicPieceTable(Position position, Piece piece)
        {
            int index = GetPositionIndex(position, piece.Colour);
            return piece.Type switch
            {
                PieceType.Pawn => PawnTable[index],
                PieceType.Knight => KnightTable[index],
                PieceType.Bishop => BishopTable[index],
                PieceType.Rook => RookTable[index],
                PieceType.Queen => QueenTable[index],
                _ => throw new ArgumentException($"{piece.Type} is not a supported {nameof(PieceType)}."),
            };
        }

        public static int GetKingPieceTable(Position position, Colour colour, bool isLateGameTableWanted)
        {
            int index = GetPositionIndex(position, colour);
            return isLateGameTableWanted ? KingLateTable[index] : KingEarlyAndMiddleTable[index];
        }

        private static int GetPositionIndex(Position position, Colour colour)
        {
            return colour == Colour.White
                ? (BaseWhenReadingWhite - position.Rank) * BoardConsts.BoardSize + position.File
                : position.Rank * BoardConsts.BoardSize + position.File;
        }
